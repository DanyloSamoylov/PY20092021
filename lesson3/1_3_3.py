a = 5
b = 3

c = a + b  # addition
print('Addition:', c)

c = a - b  # subtraction
print('Subtraction:', c)

c = a / b  # division
print('Division:', c)

c = a * b  # Multiplication
print('Multiplication:', c)

c = a ** b  # Exponent
print('Exponent:', c)

c = a % b  # Modulus
print('Modulus:', c)

c = a // b  # Floor division
print('Floor division:', c)
